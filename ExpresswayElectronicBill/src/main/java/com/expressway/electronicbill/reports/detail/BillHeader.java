package com.expressway.electronicbill.reports.detail;

public class BillHeader {
	private String ivcnodecode;
	private String user;
	private String date;
	private String industry;
	private String industrytype;
	private String billtype;
	private String billreg;
	private String billno;
	private String cncldate;
	private String cnclauthor;

	public String getIvcnodecode() {
		return ivcnodecode;
	}

	public void setIvcnodecode(String ivcnodecode) {
		this.ivcnodecode = ivcnodecode;
	}

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getIndustry() {
		return industry;
	}

	public void setIndustry(String industry) {
		this.industry = industry;
	}

	public String getIndustrytype() {
		return industrytype;
	}

	public void setIndustrytype(String industrytype) {
		this.industrytype = industrytype;
	}

	public String getBilltype() {
		return billtype;
	}

	public void setBilltype(String billtype) {
		this.billtype = billtype;
	}

	public String getBillreg() {
		return billreg;
	}

	public void setBillreg(String billreg) {
		this.billreg = billreg;
	}

	public String getBillno() {
		return billno;
	}

	public void setBillno(String billno) {
		this.billno = billno;
	}

	public String getCncldate() {
		return cncldate;
	}

	public void setCncldate(String cncldate) {
		this.cncldate = cncldate;
	}

	public String getCnclauthor() {
		return cnclauthor;
	}

	public void setCnclauthor(String cnclauthor) {
		this.cnclauthor = cnclauthor;
	}
}
